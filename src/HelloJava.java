// 파일 이름 (HelloJava)와 클래스명이 같아야 함
public class HelloJava { // HelloJava 클래스 범위 시작

	// 프로그램이 시작되는 시작점 (프로그램 실행 시 시작 시점인 main을 찾는다)
	// 메서드
	public static void main(String[] args) {
		System.out.println("hello java"); // 값을 콘솔에 출력하는 기능, java는 문장이 끝날 때 ';'으로 끝남
	} // main() 메서드 범위 끝
} // {} 블록(block)은 클래스의 시작과 끝을 나타낸다

/*
* 클래스 안에는 메서드가 있고 메서드 안에는 코드가 있다
* */